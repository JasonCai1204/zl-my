     */
    public function index(Request $request)
    {
        $news = App\News::orderby('published_at','desc')
                ->take(15)
                ->get();

        return view('web.news.news',[
              'news' =>$news
            ]);
    }

    public function loadMore(Request $request)
    {
       $news = App\News::orderby('published_at','desc')
           ->skip(15+($request->counter-1)*10)
           ->take(11)
           ->get();

       return collect([
            'data' => $news
        ])->toJson();
    }

